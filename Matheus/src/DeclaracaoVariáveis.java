public class DeclaracaoVariáveis {
    public static void main(String[] args) {
        int numeroUm = 20; // numero inteiro
        float numeroDois = 15.5f; // numero reais
        String nome = "Matheus"; // conjunto de caractere
        boolean vf =   true; // ou false
        System.out.println("Este é meu número inteiro: "+ numeroUm);
        System.out.println("Este é meu número real: "+ numeroDois);
        System.out.println("Este é meu nome: "+ nome);
        System.out.println("Este é meu valor lógico: "+ vf);

    }
}
